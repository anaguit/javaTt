package Calculadora;
public class Calculadora {
    public static int sumar(int numA, int numB){
        return numA+numB;
    }
    public static int restar(int numA, int numB){
        return numA-numB;
    }
    public static int dividir(int numA, int numB){
        return numA/numB;
    }
    public static int multiplicar(int numA, int numB){
        return numA*numB;
    }
}
